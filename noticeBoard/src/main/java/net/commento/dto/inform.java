package net.commento.dto;

public class inform {
	String name;
	String id;
	String password;

	public String getname() {
		return name;
	}
	public void setname(String name) {
		this.name=name;
	}
	public String getId() {
		return id;
	}
	public void setId(String newid) {
		this.id=newid;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String newPass) {
		this.password=newPass;
	}


}
